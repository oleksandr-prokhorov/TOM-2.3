function OnPlayerCommandText(playerid, cmdtext)
	local cmd, params = GetCommand(cmdtext);
	if cmd == "//ss" then
	--SS_testing(playerid)
		if Player[playerid].SS ~= true then
			if Player[playerid].SS_already ~= true then
			SS_Startup(playerid)
				local result, _password = sscanf(params, "s");
				if result == 1 and SHA256(string.upper(_password)) == "0530" then
					SS_Startup(playerid);
				end
			else
				SS_Startup(playerid);
			end
		else
			SS_Quit(playerid);
		end
	end
	if Player[playerid].SS == true then
		if cmd == "//new" or cmd == "//n" then
			local x, y, z = GetPlayerPos(playerid);
			print(x, y, z)
			SS_NewVob(playerid, params);
		elseif cmd == "//help" then
			SS_Help(playerid);
		elseif cmd == "//del" then
			SS_Delete(playerid);
		elseif cmd == "//b" then
			SS_BackToCharacter(playerid);
		elseif cmd == "//v" then
			SS_View(playerid);
		elseif cmd == "//c" then
			SS_Copy(playerid);
		elseif cmd == "//d" then
			SS_Dump(playerid, params);
		elseif cmd == "//s" then
			SS_Save(playerid, params);
		elseif cmd == "//dis" then
			SS_Display(playerid);
		end
	end
end

