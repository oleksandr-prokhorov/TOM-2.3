function SS_LoadSavedVobs()
	--print('1');
	local ctr = 0
	for _ in io.lines 'Vobs/Saved.txt' do
		ctr = ctr + 1;
	end
	--print('1');
	local vobload = io.open("Vobs/Saved.txt","r+");
	local temporary = nil;
	--print('1');
	for i = 0, ctr do
		temporary = vobload:read("*l");
		nowid = nil;
		--print('1');
		if temporary ~= nil then
			local sult, x, y, z, r_x, r_y, r_z, model, wrld = sscanf(temporary, "ffffffss");
			if sult == 1 then
				--print('1');
				local _n = #SavedVobs;
				SavedVobs[(_n+1)] = Vob.Create(model, wrld, x, y, z);
				SavedVobs[(_n+1)]:SetRotation(r_x, r_y, r_z);
			end
		end
	end
	vobload:close();
end

